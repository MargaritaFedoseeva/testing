public class Results {
    private double c;
    private boolean flag;
    private String opType = "";

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }


    public boolean isFlag() {
        return flag;
    }

    public void setFlag(boolean flag) {
        this.flag = flag;
    }

    public String getOpType() {
        return opType;
    }

    public void setOpType(String opType) {
        this.opType = opType;
    }
}
